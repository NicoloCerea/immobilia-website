const LANG_NAMES = { it: "Italiano", en: "English", es: "Español", fr: "Français", de: "Deutsch", ru: "Русский", zh: "中文" };

function setLanguage(lang) {
  if (!I18N[lang]) lang = "it";
  localStorage.setItem("immobilia_lang", lang);
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    if (I18N[lang][key]) el.textContent = I18N[lang][key];
  });
  const sel = document.getElementById("lang-select");
  if (sel) sel.value = lang;
}

document.addEventListener("DOMContentLoaded", function () {
  const saved = localStorage.getItem("immobilia_lang") || "it";
  setLanguage(saved);
  const sel = document.getElementById("lang-select");
  if (sel) {
    sel.addEventListener("change", function (e) {
      setLanguage(e.target.value);
    });
  }
});
